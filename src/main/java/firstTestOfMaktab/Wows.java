import java.util.Scanner;

public class Wows {


    public static void main(String[] args) {

        Scanner scanner = new Scanner(System.in);
        int num = scanner.nextInt();
        System.out.printf("w");
        for (int i = 0; i < num; i++) {
            System.out.printf("o");
        }
        System.out.printf("w!");

    }
}
